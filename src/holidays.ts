export function isDateCustomHoliday(date: Date, region: string): boolean {
  const year = date.getFullYear();
  const dateStr = `${year}-${String(date.getMonth() + 1).padStart(2, '0')}-${String(date.getDate()).padStart(2, '0')}`;
  const regionData = CUSTOM_HOLIDAYS[region];
  if (!regionData || !regionData[year]) return false;
  return dateStr in regionData[year].holidays;
}

export function isDateCustomWorkday(date: Date, region: string): boolean {
  const year = date.getFullYear();
  const dateStr = `${year}-${String(date.getMonth() + 1).padStart(2, '0')}-${String(date.getDate()).padStart(2, '0')}`;
  const regionData = CUSTOM_HOLIDAYS[region];
  if (!regionData || !regionData[year]) return false;
  return regionData[year].workdays.includes(dateStr);
}

export function getCustomHolidayName(date: Date, region: string): string | null {
  const year = date.getFullYear();
  const dateStr = `${year}-${String(date.getMonth() + 1).padStart(2, '0')}-${String(date.getDate()).padStart(2, '0')}`;
  const regionData = CUSTOM_HOLIDAYS[region];
  if (!regionData || !regionData[year]) return null;
  return regionData[year].holidays[dateStr] || null;
}

